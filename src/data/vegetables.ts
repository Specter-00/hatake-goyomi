/**
 * カタカナ→ひらがな変換（検索用の正規化）
 */
export function toHiragana(str: string): string {
  return str.replace(/[\u30A1-\u30F6]/g, ch =>
    String.fromCharCode(ch.charCodeAt(0) - 0x60)
  );
}

/**
 * 検索クエリにマッチするか（名前・かな・カテゴリを横断、かな正規化つき）
 */
export function matchesVegetable(m: VegetableMaster, query: string): boolean {
  if (!query) return true;
  const q = toHiragana(query.trim());
  return (
    m.name.includes(query.trim()) ||
    toHiragana(m.name).includes(q) ||
    m.kana.includes(q) ||
    m.category.includes(query.trim())
  );
}

/**
 * 今月まける／植えられる野菜か
 */
export function isPlantableThisMonth(m: VegetableMaster, month?: number): boolean {
  const mo = month ?? (new Date().getMonth() + 1);
  return m.sowingMonths.includes(mo) || m.plantingMonths.includes(mo);
}

