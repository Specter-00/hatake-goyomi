/* ============ ステージ・作業スケジュール自動生成 ============ */

// カテゴリごとの成長ステージ比率（栽培日数に対する割合）
function generateStages(cat: string, days: number, hasPlanting: boolean): { stage: GrowthStage; daysFromStart: number }[] {
  const pct = (p: number) => Math.round(days * p);
  if (cat === '果菜類') {
    return [
      { stage: 'seed', daysFromStart: 0 },
      { stage: 'sprout', daysFromStart: pct(0.07) },
      { stage: 'seedling', daysFromStart: pct(0.2) },
      { stage: 'transplant', daysFromStart: pct(0.33) },
      { stage: 'support', daysFromStart: pct(0.45) },
      { stage: 'flowering', daysFromStart: pct(0.6) },
      { stage: 'fruiting', daysFromStart: pct(0.75) },
      { stage: 'harvest', daysFromStart: days },
    ];
  }
  if (cat === '豆類') {
    return [
      { stage: 'seed', daysFromStart: 0 },
      { stage: 'sprout', daysFromStart: pct(0.08) },
      { stage: 'seedling', daysFromStart: pct(0.25) },
      { stage: 'flowering', daysFromStart: pct(0.55) },
      { stage: 'fruiting', daysFromStart: pct(0.72) },
      { stage: 'harvest', daysFromStart: days },
    ];
  }
  // 根菜・葉菜・ハーブ・その他
  const base: { stage: GrowthStage; daysFromStart: number }[] = [
    { stage: 'seed', daysFromStart: 0 },
    { stage: 'sprout', daysFromStart: pct(0.1) },
    { stage: 'seedling', daysFromStart: pct(0.3) },
  ];
  if (hasPlanting) base.push({ stage: 'transplant', daysFromStart: pct(0.45) });
  base.push({ stage: 'harvest', daysFromStart: days });
  return base;
}

// カテゴリごとの作業スケジュール
function generateTasks(cat: string): { type: TaskType; intervalDays: number; startDayFromPlanting: number }[] {
  const water = (interval: number) => ({ type: 'water' as TaskType, intervalDays: interval, startDayFromPlanting: 0 });
  const fert = (interval: number, start: number) => ({ type: 'fertilize' as TaskType, intervalDays: interval, startDayFromPlanting: start });
  const pest = () => ({ type: 'pest' as TaskType, intervalDays: 7, startDayFromPlanting: 0 });

  switch (cat) {
    case '果菜類': return [water(2), fert(14, 21), pest()];
    case '豆類':   return [water(2), pest()];
    case '葉菜類': return [water(2), fert(21, 21), pest()];
    case '根菜類': return [water(4), fert(21, 21)];
    case 'ハーブ類': return [water(2)];
    default:       return [water(2)];
  }
}

export const VEGETABLE_MASTERS: VegetableMaster[] = R.map(
  ([id, name, kana, emoji, category, dMin, dMax, sow, plant, harvest, difficulty, tips]) => ({
    id, name, kana, emoji, category, difficulty,
    daysToHarvest: { min: dMin, max: dMax },
    sowingMonths: sow,
    plantingMonths: plant,
    harvestMonths: harvest,
    stages: generateStages(category, dMin, plant.length > 0),
    taskSchedule: generateTasks(category),
    tips,
  })
);

export const getVegetableMaster = (id: string): VegetableMaster | undefined =>
  VEGETABLE_MASTERS.find(v => v.id === id);

