export function getArticlesByCategory(category: LibraryCategory): LibraryArticle[] {
  return LIBRARY_ARTICLES.filter(a => a.category === category);
}

export function searchArticles(query: string): LibraryArticle[] {
  if (!query.trim()) return LIBRARY_ARTICLES;
  const q = query.trim();
  return LIBRARY_ARTICLES.filter(a =>
    a.title.includes(q) ||
    a.summary.includes(q) ||
    a.sections.some(s => s.heading.includes(q) || s.body.includes(q))
  );
}
